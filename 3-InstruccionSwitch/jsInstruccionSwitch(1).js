function mostrar()
{
//tomo la edad  
var mes = document.getElementById('mes').value;

switch (new Date().getmes()) {
    case 0:
        mes = "Enero";
        break;
    case 1:
        mes = "Marzo";
        break;
    case 2:
        mes = "Julio";
        break;
    case 3:
        mes = "Diciembre";
        break;

}




}//FIN DE LA FUNCIÃ“N